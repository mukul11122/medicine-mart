// Therapeutic group mapping based on drug code ranges
function getCategory(drugCode: number): string {
  if (drugCode >= 1 && drugCode <= 28) return 'analgesics'
  if (drugCode >= 29 && drugCode <= 109) return 'antibiotics'
  if (drugCode >= 110 && drugCode <= 132) return 'dermatology'
  if (drugCode >= 133 && drugCode <= 164) return 'diabetes'
  if (drugCode >= 170 && drugCode <= 218) return 'antacids'
  if (drugCode >= 220 && drugCode <= 232) return 'vitamins'
  if (drugCode >= 235 && drugCode <= 261) return 'respiratory'
  if (drugCode >= 263 && drugCode <= 459) return 'cardiovascular'
  if (drugCode >= 461 && drugCode <= 611) return 'dermatology'
  if (drugCode >= 613 && drugCode <= 710) return 'musculoskeletal'
  if (drugCode >= 711 && drugCode <= 800) return 'pediatrics'
  if (drugCode >= 801 && drugCode <= 999) return 'antibiotics'
  if (drugCode >= 1000 && drugCode <= 1199) return 'cardiovascular'
  if (drugCode >= 1200 && drugCode <= 1399) return 'dermatology'
  if (drugCode >= 1400 && drugCode <= 1599) return 'antibiotics'
  if (drugCode >= 1600 && drugCode <= 1799) return 'cardiovascular'
  if (drugCode >= 1800 && drugCode <= 1999) return 'antibiotics'
  if (drugCode >= 2000 && drugCode <= 2165) return 'diabetes'
  return 'vitamins'
}

// Stock ranges by category
function getStock(category: string): number {
  const stockRanges: Record<string, [number, number]> = {
    analgesics: [100, 500],
    antibiotics: [50, 300],
    dermatology: [30, 200],
    diabetes: [100, 500],
    antacids: [100, 400],
    vitamins: [100, 500],
    respiratory: [50, 300],
    cardiovascular: [50, 400],
    musculoskeletal: [30, 200],
    pediatrics: [50, 200],
  }
  const [min, max] = stockRanges[category] || [50, 200]
  return Math.floor(Math.random() * (max - min + 1)) + min
}

// Generate slug from name
function slugify(name: string, code: number): string {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '') + '-' + code
}

// Determine if prescription required
function isPrescriptionRequired(code: number): boolean {
  // Antibiotics, cardiovascular, diabetes, oncology, hormones
  if (code >= 29 && code <= 109) return true
  if (code >= 133 && code <= 164) return true
  if (code >= 263 && code <= 459) return true
  if (code >= 801 && code <= 999) return true
  if (code >= 1000 && code <= 1199) return true
  if (code >= 1400 && code <= 1599) return true
  if (code >= 1800 && code <= 1999) return true
  return false
}

async function main() {
  console.log('Seeding complete Jan Aushadhi product catalog...')
  console.log(`Total products to add: ${products.length}`)

  const categories = await prisma.category.findMany()
  const categoryMap: Record<string, string> = {}
  for (const cat of categories) {
    categoryMap[cat.slug] = cat.id
  }

  let created = 0
  let skipped = 0

  for (const [code, name, packSize, mrp] of products) {
    const categorySlug = getCategory(code)
    const categoryId = categoryMap[categorySlug] || categoryMap['analgesics']

    // Calculate selling price with Jan Aushadhi discount (typically 30-50% less than branded)
    const sellingPrice = Math.round((mrp * 0.85) * 100) / 100
    const discount = Math.round(((mrp - sellingPrice) / mrp) * 100)

    // Generate batch number
    const batchNumber = `JA${code.toString().padStart(4, '0')}${String.fromCharCode(65 + Math.floor(Math.random() * 26))}${Math.floor(Math.random() * 100)}`

    const slug = slugify(name, code)
    const sku = `JA-${code.toString().padStart(4, '0')}`

    try {
      await prisma.medicine.upsert({
        where: { slug },
        update: {},
        create: {
          sku,
          name,
          genericName: name.split(' ').slice(0, 3).join(' '),
          slug,
          composition: name,
          manufacturer: 'Jan Aushadhi (PMBI)',
          categoryId,
          packSize,
          mrp,
          sellingPrice,
          gst: 12,
          discount,
          stockQuantity: getStock(categorySlug),
          minimumStockLevel: 20,
          description: `Jan Aushadhi generic medicine - ${name}`,
          dosageInfo: 'As directed by physician',
          benefits: 'Affordable generic alternative to branded medicines',
          sideEffects: 'Consult physician for side effects',
          prescriptionRequired: isPrescriptionRequired(code),
          expiryDate: new Date('2027-12-31'),
          batchNumber,
          isActive: true,
          isFeatured: code <= 30,
          rating: 4.0 + Math.random() * 0.8,
          reviewCount: Math.floor(Math.random() * 200) + 10,
        },
      })
      created++
    } catch (e: any) {
      if (e.code === 'P2002') {
        skipped++
      } else {
        console.error(`Error with ${name}: ${e.message}`)
      }
    }
  }

  console.log(`\nSeed completed!`)
  console.log(`- Products created: ${created}`)
  console.log(`- Products skipped (already exist): ${skipped}`)
  console.log(`- Total in database: ${created + skipped}`)

  const count = await prisma.medicine.count()
  console.log(`- Database total medicines: ${count}`)
}

